// '#' = filled, '.' = empty
function parse(art: string): boolean[][] {
  return art
    .trim()
    .split("\n")
    .map((line) => [...line].map((c) => c === "#"));
}

// 좌우 대칭 패턴 (짝수 너비 전용): 왼쪽 절반 → 미러링
function mirror(half: string[]): boolean[][] {
  return half.map((line) => {
    const left = [...line].map((c) => c === "#");
    const right = [...left].reverse();
    return [...left, ...right];
  });
}

// 좌우 대칭 패턴 (홀수 너비 전용): 왼쪽 절반+중앙 → 미러링 (중앙 제외)
function mirrorOdd(half: string[]): boolean[][] {
  return half.map((line) => {
    const chars = [...line].map((c) => c === "#");
    const left = chars.slice(0, -1);
    const center = chars[chars.length - 1];
    const right = [...left].reverse();
    return [...left, center, ...right];
  });
}

